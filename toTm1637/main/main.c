#include "esp_spiffs.h"
#include "esp_vfs.h"
#include "esp_log.h"

#include "status.h"

static char *TAG = "MAIN";

QueueHandle_t xQueueCommand;
QueueHandle_t xQueueStatus;

static void listSPIFFS(char * path) {
	DIR* dir = opendir(path);
	assert(dir != NULL);
	while (true) {
		struct dirent*pe = readdir(dir);
		if (!pe) break;
		ESP_LOGI(__FUNCTION__,"d_name=%s d_ino=%d d_type=%x", pe->d_name,pe->d_ino, pe->d_type);
	}
	closedir(dir);
}

esp_err_t mountSPIFFS(char * path, char * label, int max_files) {
	esp_vfs_spiffs_conf_t conf = {
		.base_path = path,
		.partition_label = label,
		.max_files = max_files,
		.format_if_mount_failed = true
	};

	// Use settings defined above to initialize and mount SPIFFS filesystem.
	// Note: esp_vfs_spiffs_register is an all-in-one convenience function.
	esp_err_t ret = esp_vfs_spiffs_register(&conf);

	if (ret != ESP_OK) {
		if (ret ==ESP_FAIL) {
			ESP_LOGE(TAG, "Failed to mount or format filesystem");
		} else if (ret== ESP_ERR_NOT_FOUND) {
			ESP_LOGE(TAG, "Failed to find SPIFFS partition");
		} else {
			ESP_LOGE(TAG, "Failed to initialize SPIFFS (%s)",esp_err_to_name(ret));
		}
		return ret;
	}

#if 0
	ESP_LOGI(TAG, "Performing SPIFFS_check().");
	ret = esp_spiffs_check(conf.partition_label);
	if (ret != ESP_OK) {
		ESP_LOGE(TAG, "SPIFFS_check() failed (%s)", esp_err_to_name(ret));
		return ret;
	} else {
			ESP_LOGI(TAG, "SPIFFS_check() successful");
	}
#endif

	size_t total = 0, used = 0;
	ret = esp_spiffs_info(conf.partition_label, &total, &used);
	if (ret != ESP_OK) {
		ESP_LOGE(TAG,"Failed to get SPIFFS partition information (%s)",esp_err_to_name(ret));
	} else {
		ESP_LOGI(TAG,"Mount %s to %s success", path, label);
		ESP_LOGI(TAG,"Partition size: total: %d, used: %d", total, used);
	}

	return ret;
}

void keyin(void *pvParameters);
void radio(void *pvParameters);
void led(void *pvParameters);

void app_main()
{
	// Initialize NVS
	esp_err_t err = nvs_flash_init();
	if (err == ESP_ERR_NVS_NO_FREE_PAGES || err == ESP_ERR_NVS_NEW_VERSION_FOUND) {
		// NVS partition was truncated and needs to be erased
		// Retry nvs_flash_init
		ESP_ERROR_CHECK(nvs_flash_erase());
		err = nvs_flash_init();
	}
	ESP_ERROR_CHECK( err );

	// Initialize SPIFFS
	// Maximum files that could be open at the same time is 1.
	char *base_path = "/preset";
	ESP_ERROR_CHECK(mountSPIFFS(base_path, "storage", 1));
	listSPIFFS(base_path);

	// Create Queue
	xQueueCommand = xQueueCreate(10, sizeof(char));
	configASSERT( xQueueCommand );
    xQueueStatus = xQueueCreate(1, sizeof(STATUS_t));
    configASSERT( xQueueStatus );

	// Create Task
	xTaskCreate(keyin, "KEYIN", 1024*4, NULL, 5, NULL);
	xTaskCreate(radio, "RADIO", 1024*4, base_path, 5, NULL);
	xTaskCreate(led, "LED", 1024*4, NULL, 5, NULL);

	while(1) {
		vTaskDelay(1);
	}
}
